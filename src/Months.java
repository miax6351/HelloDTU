public class Months {



    public static void main(String[] args) {

        int x = 2;

        for (int i = 0; i < 10; i++){
            System.out.println(i);
        }

        switch (x) {

            case 1: System.out.println("1");
            case 2: System.out.println("2");
            break;
            default: System.out.println("3");

        }

    }
}
